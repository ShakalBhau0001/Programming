// let keyword example
// This program demonstrates the use of the `let` keyword in Rust to declare variables. 

fn main()
{
    let comstr= "Shakal";
    let f = 4.5;
    let g_bool = true;
    let ichar = 'a';
    println!("Name is :{}\n Float is :{}\n Boolean is :{}\n Char is :{}", comstr, f, g_bool, ichar);
}
